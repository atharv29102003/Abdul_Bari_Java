
import java.util.Scanner;

// Sum of two numbers
public class Second {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a, b, c;
        a = sc.nextInt();
        b = sc.nextInt();
        c = a + b;
        System.out.println("The sum is : " + c);
        sc.close();
        sc.close();
    }
}
